package JAVA_PROGRAMS.Questions;

public class Sum_Numbers_3 {
    public static void main(String[] args) {
//      Sum of two integer values
        int a = 13;
        int b = 23;
        int sum = a + b;
        System.out.println(sum);

//      Sum of two floating values
        float num1 = 12.5F;
        float num2 = 21.1F;
        float sumOfNumber = num1 + num2;
        System.out.println(sumOfNumber);
    }
}
