package JAVA_PROGRAMS.Topics.Variables_1;

public class Variables {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a);
        System.out.println(b);

        a = 12;
        System.out.println(a);

        a = b;
        System.out.println(a);
    }
}
