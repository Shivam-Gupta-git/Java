// Type Conversion............

// -> type conversion = type conversion is changing a variable one data type to another data type.
// -> There are two types of type conversion in Java:
// 1. Implicit Type Conversion = Done automatically by Java. when we assign a smaller data type to a bigger data type
// 2. Explicit Type Conversion = Done manually by the programmer using cast operator. when we assign a bigger data
//    type to a smaller data type

// -> Type Conversion Hierarchy in Java
// byte → short → int → long → float → double

// Type Casting.............

// -> type casting = type casting is changing a variable one data type to another data type . but java can't be allow.
//    type casting is a type of narrowing or explicit conversion.

