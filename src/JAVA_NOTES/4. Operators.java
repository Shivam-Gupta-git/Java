// Operator........

// -> Operator = an operator is a symbol that performs a specific operation on one, two, or three operands
// (variables/values) and produces a result.

// types of operator..............

// 1. Arithmetic Operators = It is Used for mathematical operation.
//  •	+ → Addition
//	•	- → Subtraction
//	•	* → Multiplication
//	•	/ → Division (quotient)
//	•	% → Modulus (remainder)

// 2. Unary Operators = It is used for Work on a single operand
//	•	+ → Unary plus (indicates positive value)
//	•	- → Unary minus (negates value)
//	•	++ → Increment (increases value by 1)
//	•	-- → Decrement (decreases value by 1)
//	•	! → Logical NOT

// 3. Assignment Operators = It is Used to assign values.
//	•	= → Simple assignment
//	•	+= → Add and assign
//	•	-= → Subtract and assign
//	•	*= → Multiply and assign
//	•	/= → Divide and assign
//	•	%= → Modulus and assign

// 4. Relational (Comparison) Operators = It is Used to compare two values.
//	•	== → Equal to
//	•	!= → Not equal to
//	•	> → Greater than
//	•	< → Less than
//	•	>= → Greater than or equal to
//	•	<= → Less than or equal to

// 5. Logical Operators = It is Used for logical conditions.
//	•	&& → Logical AND (T-T = T, T-F = F, F-T = F, F-F = F)
//	•	|| → Logical OR  (T-T = T, T-F = T, F-T = T, F-F = F)
//	•	! → Logical NOT

// 6. Bitwise Operators = It is Work on bits (0 and 1).
//	•	& → Bitwise AND
//	•	| → Bitwise OR
//	•	^ → Bitwise XOR
//	•	~ → Bitwise Complement
//	•	<< → Left shift
//	•	>> → Right shift
//	•	>>> → Unsigned right shift

//7. Ternary Operator = It is used to Short form of if-else.
//Syntax: condition ? value_if_true : value_if_false